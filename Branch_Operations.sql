CREATE TABLE C (
    Branch_ID SERIAL PRIMARY KEY,
    Branch_Name VARCHAR(100),
    City VARCHAR(100),
    State VARCHAR(100),
    Zip_Code INTEGER,
    Manager_Name VARCHAR(100),
    Established_Date DATE
);

CREATE TABLE Branch_Operations.Employees (
    Employee_ID SERIAL PRIMARY KEY,
    Name VARCHAR(100),
    Position VARCHAR(50),
    Branch_ID INTEGER REFERENCES Branch_Operations.Branches(Branch_ID),
    Hire_Date DATE,
    Salary DECIMAL(15, 2),
    Contact_Number BIGINT,
    Email VARCHAR(100)
);



CREATE TABLE Branch_Operations.Audit_Logs (
    Log_ID SERIAL PRIMARY KEY,
    Entity_Type VARCHAR(50),
    Employee_ID INTEGER REFERENCES Branch_Operations.Employees(Employee_ID),
    Action_Type VARCHAR(10) CHECK (Action_Type IN ('INSERT', 'UPDATE', 'DELETE')),
    User_ID INTEGER,
    Timestamp TIMESTAMP
);

